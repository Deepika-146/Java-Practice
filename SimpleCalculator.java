package Practice;

public class SimpleCalculator {

	public static void main(String[] args) {
		int a=9, b=5;
		char operator='-';
switch (operator)
{
case '+': System.out.println(a+b); break;
case '-': System.out.println(a-b); break;
case '*': System.out.println(a*b); break;
case '/': System.out.println(a/b); break;
}
	}
}
